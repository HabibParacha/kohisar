    {


        DB::beginTransaction();// Start a transaction

        try {
            $partyWarehouse = PartyWarehouse::find($id);

            $partyWarehouse->delete();// Delete the partyWarehouse record

            DB::commit();// Commit the transaction
            
            return response()->json([
                'success' => true,
                'message' => 'Party Warehouse Delete successfully.',
                ],200);
                
        } catch (\Exception $e) {
          
